# int = integer
# a int is all numbers without decimal spaces
# Addition (+): Adds two numbers together.
# Subtraction (-): Subtracts the second number from the first.
# Multiplication (*): Multiplies two numbers.
# Exponentiation (**): Raises the first number to the power of the second.
# Division (/): Divides the first number by the second. The result is always a float = floating number. number with numbers after the decimal
# Floor Division (//): Divides and returns the largest integer less than or equal to the result. will always return a int
#^ It divides two numbers and gives you the largest whole number that is less than or equal to the result.
#^^ In short, floor division drops any decimal part and rounds down!
# Modulus (%): Returns the remainder of the division of the first number by the second. 